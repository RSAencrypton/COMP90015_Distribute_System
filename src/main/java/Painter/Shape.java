package Painter;

public class Shape {

    public int x;
    public int y;
    public int endX;
    public int endY;
    public int stroke;

    public Shape(int x, int y, int endX, int endY, int stroke) {
        this.x = x;
        this.y = y;
        this.endX = endX;
        this.endY = endY;
        this.stroke = stroke;
    }
}
